package SimpleSimulation.engine.simulation;

public class Particle {
    public float x;
    public float y;
    public float vX;
    public float vY;

    public Particle(float x, float y, float vX, float vY){
        this.x = x;
        this.y = y;
        this.vX = vX;
        this.vY = vY;
    }
}
